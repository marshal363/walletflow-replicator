export const addWallet = mutation({
  args: {
    accountId: v.id("accounts"),
    name: v.string(),
    type: v.union(v.literal("spending"), v.literal("savings"), v.literal("multisig")),
    currency: v.string(),
    networkIdentities: v.union(
      v.object({
        type: v.literal("spending"),
        lightning: v.string(),
        nostr: v.string(),
      }),
      v.object({
        type: v.literal("savings"),
        bitcoinAddress: v.string(),
        derivationPath: v.string(),
      }),
      v.object({
        type: v.literal("multisig"),
        addresses: v.array(v.object({
          address: v.string(),
          signers: v.array(v.object({
            pubKey: v.string(),
            weight: v.number(),
          })),
          requiredSignatures: v.number(),
        })),
        scriptType: v.union(
          v.literal("p2sh"),
          v.literal("p2wsh"),
          v.literal("p2tr")
        ),
      })
    ),
  },
  handler: async (ctx, args) => {
    // Validate account exists
    const account = await ctx.db.get(args.accountId);
    if (!account) {
      throw new Error("Account not found");
    }

    // Validate network identities match wallet type
    if (args.type !== args.networkIdentities.type) {
      throw new Error("Network identities type must match wallet type");
    }

    // Add multisig validation
    if (args.type === "multisig") {
      validateMultisigSetup(args.networkIdentities.addresses);
    }

    // Create the wallet
    const walletId = await ctx.db.insert("wallets", {
      accountId: args.accountId,
      name: args.name,
      type: args.type,
      balance: 0, // New wallets start with 0 balance
      currency: args.currency,
      lastUpdated: new Date().toISOString(),
      networkIdentities: args.networkIdentities,
    });

    return walletId;
  },
});

