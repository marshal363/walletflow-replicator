// Add these validation utilities
function validateMultisigSetup(
  addresses: Array<{
    signers: Array<{ weight: number }>;
    requiredSignatures: number;
  }>
) {
  for (const address of addresses) {
    const totalWeight = address.signers.reduce((sum, signer) => sum + signer.weight, 0);
    if (totalWeight < address.requiredSignatures) {
      throw new Error(
        `Invalid multisig setup: total weight (${totalWeight}) must be >= required signatures (${address.requiredSignatures})`
      );
    }
    if (address.signers.length < 2) {
      throw new Error("Multisig wallet requires at least 2 signers");
    }
  }
}

export const getCurrentUserSpendingWallet = query({
  args: {},
  handler: async (ctx) => {
    const identity = await ctx.auth.getUserIdentity();
    if (!identity) {
      throw new Error("Not authenticated");
    }

    // Get user by clerk ID
    const user = await ctx.db
      .query("users")
      .withIndex("by_clerk_id", (q) => q.eq("clerkId", identity.subject))
      .first();

    if (!user) {
      throw new Error("User not found");
    }

    // Find user's personal account and spending wallet
    const account = await ctx.db
      .query("accounts")
      .withIndex("by_user_and_type", (q) => 
        q.eq("userId", user._id).eq("type", "personal")
      )
      .first();

    if (!account) {
      throw new Error("Account not found");
    }

    // Get spending wallet
    const wallet = await ctx.db
      .query("wallets")
      .withIndex("by_account_and_type", (q) =>
        q.eq("accountId", account._id).eq("type", "spending")
      )
      .first();

    if (!wallet) {
      throw new Error("Spending wallet not found");
    }

    return wallet;
  },
}); 
